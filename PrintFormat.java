public class PrintFormat {
    public static void main(String[] args) {
        boolean printB = false;
        char printC = '@';
        int printD = 1;
        double PrintF = 3.14;
        String printS = "Hello, world!";

        System.out.printf("%b", printB);
        System.out.printf("%c", printC);
        System.out.printf("%D", printD);
        System.out.printf("%f", PrintF);
        System.out.printf("%s", printS);


    }
}
